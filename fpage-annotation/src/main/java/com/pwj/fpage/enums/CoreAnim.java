/**
 * @Author: pwj
 * @Date: 2020/7/30 13:45
 * @FileName: CoreAnim
 * @Description: 页面切换动画枚举
 */
public enum  CoreAnim {
    /**
     * 没有动画
     */
    none,

    /**
     * 由下到上动画
     */
    present,

    /**
     * 从左到右动画
     */
    slide,

    /**
     * 渐变
     */
    fade,

    /**
     * 放大
     */
    zoom
}
